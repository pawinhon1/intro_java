public class ForLoop{

    public static void main(String[] args){

        System.out.println("----For Loop ----\n n++");

        for(int n=1; n<=5; n++){
            System.out.println("Num = "+n + "\n");
        }

        System.out.println("_________________________\nn--");

        for(int a=5; a>=1; a--){
            System.out.println("Num = " + a + "\n");
        }
    }
}